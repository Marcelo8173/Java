    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
       if(txtCodigo.getText().equals("")){
           JOptionPane.showMessageDialog(null, "Informe o campo de codigo");
       }else if(txtNome.getText().equals("")){
           JOptionPane.showMessageDialog(null, "Informe o nome para prosseguir");
       }else if(txtValor.getText().equals("")){
           JOptionPane.showMessageDialog(null, "Informe o campo valor para prosseguir");
       }else{
        Produto p = new Produto(Integer.parseInt(txtCodigo.getText()), txtNome.getText(), Double.parseDouble(txtValor.getText()));
        p.save("/home/marcelo/√Årea de Trabalho/a.dat");
        txtCodigo.setText("");
        txtNome.setText("");
        txtCodigo.setText("");
       }
    }//GEN-LAST:event_btnSalvarActionPerformed

